// Función de búsqueda
export function searchContent(query, language = 'es') {
  if (!query || query.trim() === '') return [];

  const searchTerm = query.toLowerCase().trim();
  const allContent = [...projectsData, ...postsData];

  return allContent.filter(item => {
    const title = item.translations[language].title.toLowerCase();
    const description = item.translations[language].description.toLowerCase();
    const tags = item.tags.map(tag => tag.toLowerCase()).join(' ');

    return (
      title.includes(searchTerm) ||
      description.includes(searchTerm) ||
      tags.includes(searchTerm)
    );
  });
}